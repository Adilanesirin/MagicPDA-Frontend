// scripts/set-build-info.js
const fs = require("fs");
const path = require("path");

const appJson = require("../app.json");
const version = appJson.expo.version;
const buildDate = new Date().toLocaleDateString("en-GB", {
  day: "2-digit", month: "short", year: "numeric"
});

const content = `// Auto-generated by set-build-info.js — do not edit manually
export const BUILD_VERSION = "${version}";
export const BUILD_DATE = "${buildDate}";
`;

fs.writeFileSync(path.join(__dirname, "../constants/buildInfo.ts"), content);
console.log(`✅ Build info set: v${version} | ${buildDate}`);